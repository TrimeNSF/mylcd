//写指令到 LCD 模块 
void transfer_command(int data1) 
{ 
  char i; 
  digitalWrite(cs, 0); 
  digitalWrite(rs, 0); 
  for(i=0;i<8;i++) 
  { 
    digitalWrite(clk, 0); 
    if(data1&0x80)
      digitalWrite(sda, 1); 
    else 
      digitalWrite(sda, 0); 
    digitalWrite(clk, 1); 
    data1<<=1; 
  } 
  digitalWrite(cs, 1); 
} 
//写数据到 LCD 模块 
void transfer_data(int data1) 
{ 
  char i; 
  digitalWrite(cs, 0); 
  digitalWrite(rs, 1); 
  for(i=0;i<8;i++) 
  { 
    digitalWrite(clk, 0); 
    if(data1&0x80)
      digitalWrite(sda, 1); 
    else 
      digitalWrite(sda, 0); 
    digitalWrite(clk, 1); 
    data1<<=1; 
  } 
  digitalWrite(cs, 1); 
} 
void waitkey() 
{ 
  usleep(3000000); 
} 
//LCD 模块初始化 
void initial_lcd() 
{ 
  digitalWrite(reset, 0); //低电平复位 
  usleep(800000); 
  digitalWrite(reset, 1); //复位完毕 
  usleep(800000); 
  transfer_command(0xe2); //软复位 
  usleep(500000); 
  transfer_command(0x2f); //打开内部升压 
  usleep(500000); 
  transfer_command(0x81); //微调对比度 
  transfer_command(0x33); //微调对比度的值，可设置范围 0x00～0xFF(原值 0X33） 
  transfer_command(0xeb); //1/9 偏压比（bias） 
  transfer_command(0xc6); //行列扫描顺序：从上到下、从左到右 
  transfer_command(0xaf); //开显示 
} 
void lcd_address(uchar page,uchar column) 
{ 
  column=column-1; //我们平常所说的第 1 列，在 LCD 驱动 IC 里是第 0 列，所以在这里减去 1。 
  page=page-1; //我们平常所说的第 1 页，在 LCD 驱动 IC 里是第 0 页，所以在这里减去 1。 
  transfer_command(0xb0+page); //设置页地址。每页是 8 行。一个画面的 64 行被分成 8 个页。 
  transfer_command(((column>>4)&0x0f)+0x10); //设置列地址的高 4 位 
  transfer_command(column&0x0f); //设置列地址的低 4 位 
} 
//全屏清屏 
void clear_screen() 
{ 
  unsigned char i,j; 
  for(i=0;i<9;i++) 
  { 
    lcd_address(1+i,1);
    for(j=0;j<192;j++) 
    { 
      transfer_data(0x01); 
    } 
  } 
} 
void display_graphic_128x64(uchar page,uchar column,uchar *dp) 
{ 
  uchar i,j; 
  for(i=0;i<8;i++) 
  { 
    lcd_address(page+i,column); 
    for(j=0;j<128;j++) 
    { 
      transfer_data(*dp); 
      dp++; 
    } 
  } 
} 
void display_graphic_16x48(uchar page,uchar column,uchar *dp) 
{ 
  uchar i,j; 
  for(i=0;i<2;i++) 
  { 
    lcd_address(page+i,column); 
    for(j=0;j<48;j++) 
    { 
      transfer_data(*dp); 
      dp++; 
    } 
  } 
} 
//显示 32x64 点阵图像、汉字、生僻字或 32x64 点阵的其他图标 
void display_graphic_32x64(uchar page,uchar column,uchar *dp) 
{ 
  uchar i,j; 
  for(j=0;j<32;j++) 
  { 
    for (i=0;i<8;i++) 
    { 
      lcd_address(page+i,column+j); 
      transfer_data(*dp); //写数据到 LCD,每写完一个 8 位的数据后列地址自动加 1 
      dp++;
    } 
  } 
} 
//显示 32x32 点阵图像、汉字、生僻字或 32x32 点阵的其他图标 
void display_graphic_32x32(uchar page,uchar column,uchar *dp) 
{ 
  uchar i,j; 
  for(j=0;j<32;j++) 
  { 
    for (i=0;i<4;i++) 
    { 
      lcd_address(page+i,column+j); 
      transfer_data(*dp); //写数据到 LCD,每写完一个 8 位的数据后列地址自动加 1 
      dp++; 
    } 
  } 
} 
void display_string_8x16(uint page,uint column,uchar *text) 
{ 
  uint i=0,j,k,n; 
  while(text[i]>0x00) 
  { 
    if((text[i]>=0x20)&&(text[i]<=0x7e)) 
    { 
      j=text[i]-0x20; 
      for(n=0;n<2;n++) 
      { 
        lcd_address(page+n,column); 
        for(k=0;k<8;k++) 
        { 
          transfer_data(ascii_table_8x16[j][k+8*n]);//显示 5x7 的 ASCII 字到 LCD 上，y 为页地址，x 为列地址，最后为数据 
        } 
      } 
      i++; 
      column+=8; 
    } 
    else 
      i++; 
  } 
} 
